#7. programa que calcule dos operandos con los 7 operadores vistos en clase. ¿Cómo puedes forzar que el resultado de la división tenga 2 decimales?
var1=float(input("Introduce un número primo"))
var2=float(input("Introduce otro número primo"))
print("La suma de los dos números es",var1+var2)
print("El resto de los dos números es",var1%var2)
print("La resta entre los dos números es",var1-var2)
print("El producto entre los dos números es",var1*var2)
print("La división entre los dos números es",var1/var2)
print("El exponente de los dos números es",var1**var2)
print("La división entera de los dos números es",var1//var2)

