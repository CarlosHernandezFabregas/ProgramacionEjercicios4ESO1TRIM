#2) Realizar un programa que el usuario introduzca un número por teclado utilizando los operadores lógicos ==,!=,>,< determine la comparación con el número 5 indicando por pantalla si el valor introducido es igual, diferente, menor o mayor
var1=int(input("Introduce un número"))
if var1==5:print("El número es igual que 5")
if var1!=5:print("El número es diferente que 5")
if var1<5:print("El número es menor que 5")
else:print("El número es mayor que 5")